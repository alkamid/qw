
public class CompoundBinary {

	String label;
	double bandgap, latticeConstant, VBO, ac, av, c11, c12, deformationPotential;
	
	public CompoundBinary(String label, double bandgap, double latticeConstant, double VBO, double ac, double av, double c11, double c12, double deformationPotential) {
	
	this.label = label;
	this.bandgap = bandgap;
	this.latticeConstant = latticeConstant;
	this.VBO = VBO;
	this.ac = ac;
	this.av = av;
	this.c11 = c11;
	this.c12 = c12;
	this.deformationPotential = deformationPotential;
	
	}
	
}
